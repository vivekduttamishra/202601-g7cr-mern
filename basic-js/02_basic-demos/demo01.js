//JS Data Types

//string, number, boolean,  undefined, object, symbol


var a = 20; //number
console.log(typeof a, a); //number

a="Hello"; //string
console.log(typeof a, a); //string

a=true;
console.log(typeof a, a); //boolean

a=new Date();
console.log(typeof a, a); //object

a=[1,2,3,4];
console.log(typeof a, a); //object

a=null;
console.log(typeof a, a); //object

var b;
console.log(typeof b, b); //undefined

c=10
console.log(typeof c, c); //number

